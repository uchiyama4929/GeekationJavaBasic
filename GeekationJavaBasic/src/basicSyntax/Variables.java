package basicSyntax;

public class Variables {
    public static void main(String[] args) {
        int num1 = 1;
        double num2 = 1.0;

        System.out.println(num1);
        System.out.println(num2);

        System.out.println("私の好きな整数は" + num1 + "です。");
        System.out.println("私の好きな少数は" + num2 + "です。");
    }
}
